public class kadai2_1_4
{
    public static void main(String[] a)
    {
        System.out.println(23 | 45);
        System.out.println(23 & 45);
        System.out.println(23 ^ 45);
        System.out.println(23 >> 45);
        System.out.println(23 << 45);
        System.out.println(23 >>> 45);//右へ3ビットシフト(符号無し)
        System.out.println(~23);
    }

}